def tta_predict(model, loader, transforms, device, num_tta=4):  # Use first 4 for demo
    all_logits = []
    with torch.no_grad():
        for imgs, labels in loader:
            imgs_orig = imgs.to(device)
            batch_logits = torch.zeros(imgs.size(0), 7, device=device)

            for tta_idx, tform in enumerate(transforms[:num_tta]):
                if tta_idx == 0:
                    with autocast():
                        logits = model(imgs_orig)
                else:
                    augmented_imgs = []
                    for img in imgs:
                        aug_img = np.array(Image.fromarray((img.cpu().numpy().transpose(1,2,0) * 255).astype(np.uint8)))
                        aug = tform(image=aug_img)
                        augmented_imgs.append(aug['image'])
                    aug_batch = torch.stack(augmented_imgs).to(device)
                    with autocast():
                        logits = model(aug_batch)

                batch_logits += logits / num_tta

            all_logits.append(batch_logits)

    all_logits = torch.cat(all_logits)
    probs = torch.softmax(all_logits, dim=1)
    preds = torch.argmax(probs, dim=1).cpu().numpy()
    return preds, probs.cpu().numpy()

