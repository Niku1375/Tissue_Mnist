import torch.nn.functional as F

def train_epoch(model, loader, optimizer, criterion, scaler, device, is_train=True, stage='A'):  # Add stage param
    if is_train:
        model.train()
    else:
        model.eval()

    running_loss = 0.0
    all_preds = []
    all_labels_list = []
    all_probs = []

    with tqdm(loader, desc="Epoch" if is_train else "Eval") as pbar:
        for batch_idx, (imgs, labels) in enumerate(pbar):
            imgs, labels = imgs.to(device), labels.to(device)

            # Fix: Cast to long for CE
            hard_labels = labels.long()

            # MixUp: Only for stage='B' and train (disabled for A)
            mixup_applied = False
            if is_train and stage == 'B' and random.random() < 0.5:
                # Define RARE_CLASSES if not global (hardcode for safety)
                RARE_CLASSES = torch.tensor([0, 3, 6], device=device)
                rare_mask = torch.isin(hard_labels, RARE_CLASSES)
                if rare_mask.any():
                    perm = torch.randperm(hard_labels.size(0), device=device)
                    lam = np.random.beta(0.2, 0.2)  # Beta inline
                    lam_tensor = torch.full((hard_labels.size(0),), lam, dtype=torch.float32, device=device)

                    mixed_imgs = lam_tensor.view(-1, 1, 1, 1) * imgs + (1 - lam_tensor.view(-1, 1, 1, 1)) * imgs[perm]
                    imgs = mixed_imgs

                    # Soft for potential, but loss on hard
                    onehot_orig = F.one_hot(hard_labels, num_classes=7).float()
                    onehot_perm = F.one_hot(hard_labels[perm], num_classes=7).float()
                    soft_labels = lam_tensor.view(-1, 1) * onehot_orig + (1 - lam_tensor.view(-1, 1)) * onehot_perm

                    mixup_applied = True
                    if batch_idx < 5:
                        print(f"MixUp applied (B batch {batch_idx}, lam={lam:.3f}, rares={rare_mask.sum().item()}/{hard_labels.size(0)})")

            optimizer.zero_grad() if is_train else None

            with torch.amp.autocast('cuda'):
                outputs = model(imgs)
                loss = criterion(outputs, hard_labels)  # Hard always for unweighted CE

            if is_train:
                scaler.scale(loss).backward()
                scaler.step(optimizer)
                scaler.update()

            running_loss += loss.item() * imgs.size(0)
            probs = torch.softmax(outputs, dim=1)
            _, preds = torch.max(outputs, 1)

            all_preds.extend(preds.cpu().numpy())
            all_labels_list.extend(hard_labels.cpu().numpy())
            all_probs.extend(probs.detach().cpu().numpy())

            if is_train:
                pbar.set_postfix({'loss': f"{loss.item():.4f}"})

    avg_loss = running_loss / len(loader.dataset)
    acc = accuracy_score(all_labels_list, all_preds)
    f1 = f1_score(all_labels_list, all_preds, average='macro', zero_division=0)

    all_probs_arr = np.array(all_probs, dtype=np.float64)
    all_probs_arr = np.clip(all_probs_arr, 0, 1)
    all_probs_arr = all_probs_arr / all_probs_arr.sum(axis=1, keepdims=True)
    all_labels = np.array(all_labels_list)

    try:
        auroc = roc_auc_score(all_labels, all_probs_arr, multi_class='ovr', average='macro')
        if not np.isfinite(auroc):
            auroc = 0.5
    except Exception as e:
        print(f"AUROC warning: {e} - Using 0.5 fallback")
        auroc = 0.5

    return avg_loss, acc, f1, auroc, all_preds, all_labels_list

